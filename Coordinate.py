

#class to represent a square on the battleshipboard
class Coordinate:
    #Initialize the class with a letter and a number
    def __init__(self, lettercord, numbercord):
        self.lettercord = lettercord
        self.numbercord = numbercord
        
    #Return the letter
    def get_letter():
        return self.lettercord
    #Return the number
    def get_number():
        return self.numbercord